package lv2;

public class Ex01 {

	public static void main(String[] args) {
		
		//반복문	while, for
		
		//반복문 종료조건
		//1.초기식
		//2.조건식
		//3.증감식
		
		int n = 0;
		while(n < 5) {
			
			System.out.println(n);
			n++;
		}

		//for(초기식; 조건식; 증감식){실행문;}
		for(int i=0; i < 5; i++) {
			System.out.println(i);
		}
		
		
	}

}
